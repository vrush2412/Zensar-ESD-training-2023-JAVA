
public class Forloop {
 public static void main(String[] args)
 //str args tells you that this method will receive an arra
 {
	for(int i=0,j=0;i<=10;i++,j--)
	{
		System.out.println(i+"\t"+j);
	}
	//i will be alive still the loop is running.semicolons are compulsary.
	// rather then System.out.println(i);
	System.out.println("Out of loop");
  }
}
